namespace Atom.Media;

/// <summary>
/// Формат представления медиаданных.
/// </summary>
public enum MediaFormat
{
    /// <summary>
    /// Неизвестный формат.
    /// </summary>
    Unknown,
    /// <summary>
    /// Формат представления цвета, который используется в цифровом видео. Он является одним из наиболее распространенных форматов для хранения и передачи видеоданных.
    /// </summary>
    YUYV,
}