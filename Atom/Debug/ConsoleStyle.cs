namespace Atom.Debug;

/// <summary>
/// Стиль записи в консоли.
/// </summary>
public enum ConsoleStyle
{
    /// <summary>
    /// Жирный.
    /// </summary>
    Bold,
    /// <summary>
    /// Не жирный.
    /// </summary>
    NoBold,
    /// <summary>
    /// Подчёркнутый.
    /// </summary>
    Underline,
    /// <summary>
    /// Не подчёркнутый.
    /// </summary>
    NoUnderline,
    /// <summary>
    /// Инвертированный цвет.
    /// </summary>
    Reverse,
    /// <summary>
    /// Не инвертированный цвет.
    /// </summary>
    NoReverse,
}