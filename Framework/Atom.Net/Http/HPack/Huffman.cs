    private static ushort[] GenerateDecodingLookupTree()
    {
        var decodingTree = new ushort[15 * 256];

        var encodingTableCodes = EncodingTableCodes;
        var encodingTableBitLengths = EncodingTableBitLengths;

        var allocatedLookupTableIndex = 0;

        for (var octet = 0; octet <= 256; ++octet)
        {
            var code = encodingTableCodes[octet];
            var bitLength = encodingTableBitLengths[octet];

            var lookupTableIndex = 0;
            var bitsLeft = bitLength;

            while (bitsLeft > 0)
            {
                var indexInLookupTable = (int)(code >> (32 - 8));

                if (bitsLeft <= 8)
                {
                    var suffixCount = 1 << (8 - bitsLeft);

                    for (var suffix = 0; suffix < suffixCount; ++suffix)
                        decodingTree[(lookupTableIndex << 8) + (indexInLookupTable | suffix)] = octet is 256 ? (ushort)0x80ff : (ushort)((bitsLeft << 8) | octet);
                }
                else
                {
                    var lookupValue = decodingTree[(lookupTableIndex << 8) + indexInLookupTable];

                    if (lookupValue is 0)
                    {
                        ++allocatedLookupTableIndex;
                        decodingTree[(lookupTableIndex << 8) + indexInLookupTable] = (ushort)((0x80 | allocatedLookupTableIndex) << 8);
                        lookupTableIndex = allocatedLookupTableIndex;
                    }
                    else
                    {
                        lookupTableIndex = (lookupValue & 0x7f00) >> 8;
                    }
                }

                bitsLeft -= 8;
                code <<= 8;
            }
        }

        return decodingTree;
    }

    public static (uint encoded, int bitLength) Encode(int data) => (EncodingTableCodes[data], EncodingTableBitLengths[data]);

    /// <summary>
    /// Decodes a Huffman encoded string from a byte array.
    /// </summary>
    /// <param name="src">The source byte array containing the encoded data.</param>
    /// <param name="dstArray">The destination byte array to store the decoded data.  This may grow if its size is insufficient.</param>
    /// <returns>The number of decoded symbols.</returns>
    public static int Decode(ReadOnlySpan<byte> src, ref byte[] dstArray)
    {
        var dst = dstArray;
        var decodingTree = s_decodingTree;

        var lookupTableIndex = 0;
        int lookupIndex;

        var acc = 0U;
        var bitsInAcc = 0;

        var i = 0;
        var j = 0;

        while (i < src.Length)
        {
            acc <<= 8;
            acc |= src[i++];
            bitsInAcc += 8;

            do
            {
                lookupIndex = (byte)(acc >> (bitsInAcc - 8));
                var lookupValue = decodingTree[(lookupTableIndex << 8) + lookupIndex];

                if (lookupValue < 0x80_00)
                {
                    if (j == dst.Length)
                    {
                        Array.Resize(ref dstArray, dst.Length * 2);
                        dst = dstArray;
                    }

                    dst[j++] = (byte)lookupValue;

                    lookupTableIndex = 0;
                    bitsInAcc -= lookupValue >> 8;
                }
                else
                {
                    lookupTableIndex = (lookupValue & 0x7f00) >> 8;
                    if (lookupTableIndex is 0) throw new HuffmanDecodingException(DecodeFailedErrorMessage);

                    bitsInAcc -= 8;
                }
            }
            while (bitsInAcc >= 8);
        }

        // Finish decoding last < 8 bits of src.
        // Processing of the last byte has to handle several corner cases
        // so it's extracted outside of the main loop for performance reasons.
        while (bitsInAcc > 0)
        {
            if (lookupTableIndex is 0)
            {
                var ones = uint.MaxValue >> (32 - bitsInAcc);
                if ((acc & ones) == ones) break;
            }

            lookupIndex = (byte)(acc << (8 - bitsInAcc));
            var lookupValue = decodingTree[(lookupTableIndex << 8) + lookupIndex];

            if (lookupValue < 0x80_00)
            {
                bitsInAcc -= lookupValue >> 8;
                if (bitsInAcc < 0) throw new HuffmanDecodingException(DecodeFailedErrorMessage);

                if (j == dst.Length)
                {
                    Array.Resize(ref dstArray, dst.Length * 2);
                    dst = dstArray;
                }

                dst[j++] = (byte)lookupValue;
                lookupTableIndex = 0;
            }
            else
            {
                throw new HuffmanDecodingException(DecodeFailedErrorMessage);
            }
        }

        if (lookupTableIndex is not 0) throw new HuffmanDecodingException(DecodeFailedErrorMessage);

        return j;
    }
